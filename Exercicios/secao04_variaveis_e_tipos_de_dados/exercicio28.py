"""28- Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos
três valores lidos."""

val1 = float(input("Defina o primeiro valor: "))
val2 = float(input("Defina o segundo valor: "))
val3 = float(input("Defina o terceito valor: "))
result = (val1 ** 2) + (val2 ** 2) + (val3 ** 3)
print(f"O resultado da soma dos quadrados dos três valores é {result}")
