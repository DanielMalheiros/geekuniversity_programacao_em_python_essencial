"""34- Leia o valor do raio de um círculo e calcule e imprima a área do círculo correspondente.
A área do circulo é pi * raio ** 2, considere pi = 3,141592."""

r = float(input("Defina o raio de um círculo em centímetros: "))
area = 3.141592 * r ** 2
print(f"A área deste círculo é {area} centímetros quadrados.")
