#  29- Leia quatro notas, calcule a média aritmética e imprima o resultado.

nota1 = float(input("Defina a primeira nota: "))
nota2 = float(input("Defina a segunda nota: "))
nota3 = float(input("Defina a terceira nota: "))
nota4 = float(input("Defina a quarta nota: "))
media = (nota1 + nota2 + nota3 + nota4) / 4
print(f"A média aritmética é {media}.")
